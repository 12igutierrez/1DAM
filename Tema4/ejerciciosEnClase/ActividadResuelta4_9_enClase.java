package ejerciciosEnClase;

public class ActividadResuelta4_9_enClase {

	public static void main(String[] args) {
		System.out.println("Suma 1: "+suma(10,20));
		System.out.println("Suma 2: "+suma(12.9,3.5));
		System.out.println("Suma 3: "+suma(20,13.2));
	}

	static int suma(int a, int b) {
		return a+b;
	}
	
	static double suma(double a, double b) {
		return a+b;
	}

}
